package cn.tedu.demo;

public class DataType {

	public static void main(String[] args) {
		//1.byte short int long  数据类型 变量名；
		byte count;
		//2.变量赋值   变量名称=数值；
		count=90;
		//3.变量的使用
		System.out.println(count);
		count=80;
		System.out.println(count);
		//变量在定义的时候同时进行赋值
		int age=20;
		System.out.println(age);
		//float double
		float a;
		a=1.4f;
		System.out.println(a);
		double b=2.3;
		System.out.println(b);
		
	}

}
